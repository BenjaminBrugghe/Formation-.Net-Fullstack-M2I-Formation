/**
 * LES OPERATEURS COMPARAISON
 */

/*
     == Signifie égal à (pour comparer 2 valeurs)
     
     === Signifie strictement égale à (si la valeur ET le type sont identiques)
     
     != Signifie différent de (pour 2 variables)
     
     !== Signifie strictement différent de (si le type OU la valeur sont différents)
 */

// EXEMPLES :
console.log(1 == 1);   // Vrai
console.log("1" == 1);   // Vrai
console.log(1 === 1);   // Vrai
console.log("1" === 1);   // Faux

console.log(1 != 1);   // Faux
console.log("1" != 1);   // Faux
console.log(1 !== 1);   // Faux
console.log("1" !== 1);   // Vrai
